public class Square extends Shape {

    public Square() {
    	System.out.println("Square");
    }

    public void display() {
        System.out.println("Square is being Displayed!");
    }

    public void fill() {
        System.out.println("Square has been filled!");
    }

    public void undisplay() {
        System.out.println("Square is being Undisplayed!");
    }

}